export class Booking{
  fullName: string ="";
  email: string ="";
  phoneNumber: string ="";
  timeSlot: string ="";

  constructor(fullName: string, email: string, phoneNumber: string, timeSlot: string) {
    this.fullName = fullName;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.timeSlot = timeSlot;
  }
}




